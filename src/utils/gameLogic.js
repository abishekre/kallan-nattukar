const getRecentWords = () => {
  try {
    return JSON.parse(localStorage.getItem('kn_recentWords') || '[]');
  } catch {
    return [];
  }
};

const saveRecentWords = (words) => {
  try {
    localStorage.setItem('kn_recentWords', JSON.stringify(words));
  } catch {
    // Storage full or unavailable — non-critical, ignore.
  }
};

export const generateGameRoles = (players, imposterCount, enablePottan, categories, customCategoriesData = {}, difficultyLevel = 'all') => {
  if (!players || players.length < 3) {
    console.error("Cannot start game with fewer than 3 players");
    return null;
  }
  let recentWords = getRecentWords();
  // 1. Determine active category and words
  const validCategories = categories.filter(cat => {
    if (WORD_BANK[cat]) return true;
    if (customCategoriesData[cat] && customCategoriesData[cat].length > 0) return true;
    return false;
  });
  
  const activeCategories = validCategories.length > 0 ? validCategories : ['Sadhya & Thattukada'];
  const randomCategory = activeCategories[Math.floor(Math.random() * activeCategories.length)];
  
  let wordPair;
  // If the selected category is a custom one and has words
  if (customCategoriesData[randomCategory] && customCategoriesData[randomCategory].length > 0) {
    let catWords = customCategoriesData[randomCategory];
    
    // Auto-inject random deterministic difficulty if missing
    catWords = catWords.map((w, idx) => ({ ...w, difficulty: w.difficulty || (idx % 3) + 1 }));

    if (difficultyLevel !== 'all') {
      const diffNum = difficultyLevel === 'hard' ? 3 : (difficultyLevel === 'medium' ? 2 : 1);
      const filtered = catWords.filter(w => w.difficulty === diffNum);
      if (filtered.length > 0) catWords = filtered;
    }

    const availableWords = catWords.filter(w => !recentWords.includes(w.nattukaran + w.kallan));
    const wordsToPickFrom = availableWords.length > 0 ? availableWords : catWords;
    wordPair = wordsToPickFrom[Math.floor(Math.random() * wordsToPickFrom.length)];
  } else {
    // Fallback if custom is empty or it's a standard category
    const fallbackCategory = WORD_BANK[randomCategory] ? randomCategory : 'Sadhya & Thattukada';
    let catWords = WORD_BANK[fallbackCategory];
    
    // Auto-inject random deterministic difficulty if missing (1=easy, 2=medium, 3=hard)
    catWords = catWords.map((w, idx) => ({ ...w, difficulty: w.difficulty || (idx % 3) + 1 }));

    if (difficultyLevel !== 'all') {
      const diffNum = difficultyLevel === 'hard' ? 3 : (difficultyLevel === 'medium' ? 2 : 1);
      const filtered = catWords.filter(w => w.difficulty === diffNum);
      if (filtered.length > 0) catWords = filtered;
    }

    const availableWords = catWords.filter(w => !recentWords.includes(w.nattukaran + w.kallan));
    const wordsToPickFrom = availableWords.length > 0 ? availableWords : catWords;
    wordPair = wordsToPickFrom[Math.floor(Math.random() * wordsToPickFrom.length)];
  }
  
  recentWords.push(wordPair.nattukaran + wordPair.kallan);
  if (recentWords.length > 25) recentWords.shift(); // increased history buffer
  saveRecentWords(recentWords);
  
  // 2. Pro-Tip Logic: Randomly swap majority/minority words to prevent pattern recognition
  const swapMajority = Math.random() > 0.5;
  const majorityWord = swapMajority ? wordPair.kallan : wordPair.nattukaran;
  const minorityWord = swapMajority ? wordPair.nattukaran : wordPair.kallan;

  // 3. Assign Roles
  let rolesPool = [];
  
  // Add Kallans
  for (let i = 0; i < imposterCount; i++) {
    rolesPool.push({ role: 'Kallan', word: minorityWord });
  }
  
  // Add Pottan if enabled (max 1 for now)
  if (enablePottan) {
    rolesPool.push({ role: 'Pottan', word: null });
  }
  
  // Fill the rest with Nattukar
  const remainingSlots = players.length - rolesPool.length;
  for (let i = 0; i < remainingSlots; i++) {
    rolesPool.push({ role: 'Nattukaran', word: majorityWord });
  }

  // 4. Shuffle roles
  for (let i = rolesPool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [rolesPool[i], rolesPool[j]] = [rolesPool[j], rolesPool[i]];
  }

  // 5. Map to players
  const roles = players.map((player, index) => ({
    id: player.id,
    name: player.name,
    role: rolesPool[index].role,
    word: rolesPool[index].word,
    eliminated: false,
    revealed: false 
  }));

  return { roles, category: randomCategory, difficulty: wordPair.difficulty || 1 };
};

export const calculateWinCondition = ({ assignedRoles, pottanStoleWin, multiRoundVoting }) => {
  const kallans = assignedRoles.filter(p => p.role === 'Kallan');
  const pottan = assignedRoles.find(p => p.role === 'Pottan');
  const nattukar = assignedRoles.filter(p => p.role === 'Nattukaran');
  
  const activeKallans = kallans.filter(p => !p.eliminated);
  const activeNattukar = nattukar.filter(p => !p.eliminated);
  const pottanAlive = pottan && !pottan.eliminated ? 1 : 0;
  
  let winner = '';
  let winReason = '';
  let winningTeam = ''; 

  if (pottanStoleWin) {
    winner = 'POTTAN WON!';
    winReason = `${pottan?.name || 'The Pottan'} correctly guessed the secret word!`;
    winningTeam = 'pottan';
  } else if (multiRoundVoting) {
    if (activeKallans.length === 0) {
      winner = 'NATTUKAR WON!';
      winReason = 'All Kallans were caught!';
      winningTeam = 'nattukar';
    } else if (activeKallans.length >= activeNattukar.length + pottanAlive) {
      winner = 'KALLANS WON!';
      winReason = 'Kallans overpowered the village!';
      winningTeam = 'kallans';
    }
  } else {
    const eliminatedPlayers = assignedRoles.filter(p => p.eliminated);
    if (eliminatedPlayers.length > 0) {
      const eliminatedKallan = eliminatedPlayers.find(p => p.role === 'Kallan');
      const wasPottanOnly = eliminatedPlayers.every(p => p.role === 'Pottan');
      if (eliminatedKallan) {
        winner = 'NATTUKAR WON!';
        winReason = `You successfully caught a Kallan! (${eliminatedKallan.name})`;
        winningTeam = 'nattukar';
      } else if (wasPottanOnly && activeKallans.length <= activeNattukar.length) {
        // Pottan voted out, guessed wrong: Kallans only win if they outnumber the Nattukar.
        winner = 'NATTUKAR WON!';
        winReason = `You voted out the Pottan (${eliminatedPlayers.map(p => p.name).join(', ')}), and their wrong guess exposed the Kallans!`;
        winningTeam = 'nattukar';
      } else {
        winner = 'KALLANS WON!';
        winReason = wasPottanOnly
          ? `You voted out the Pottan (${eliminatedPlayers.map(p => p.name).join(', ')})! The Kallans slipped away!`
          : `You killed ${eliminatedPlayers.map(p => `${p.name} (${p.role})`).join(', ')}! The Kallans escaped!`;
        winningTeam = 'kallans';
      }
    }
  }

  // Defensive fallback: the Results screen must never render an empty verdict.
  // This can only be hit by an unexpected state, but we guarantee a sane value.
  if (!winningTeam) {
    const anyKallanEliminated = assignedRoles.some(p => p.eliminated && p.role === 'Kallan');
    if (anyKallanEliminated) {
      winner = 'NATTUKAR WON!';
      winReason = 'A Kallan was caught!';
      winningTeam = 'nattukar';
    } else {
      winner = 'KALLANS WON!';
      winReason = 'The Kallans slipped away into the night!';
      winningTeam = 'kallans';
    }
  }

  const wrongfulKills = assignedRoles.filter(p => p.eliminated && p.role === 'Nattukaran');

  return { winner, winReason, winningTeam, wrongfulKills };
};

export const calculateScores = ({ assignedRoles, winningTeam, enableScoreboard }) => {
  if (!enableScoreboard || !winningTeam) return [];

  const pointAssignments = [];

  assignedRoles.forEach(p => {
    let pts = 0;
    let won = false;
    let wrongfulDeath = p.eliminated && p.role === 'Nattukaran';

    if (winningTeam === 'nattukar' && p.role === 'Nattukaran') { pts = 1; won = true; }
    if (winningTeam === 'kallans' && p.role === 'Kallan') { pts = 3; won = true; }
    if (winningTeam === 'pottan' && p.role === 'Pottan') { pts = 5; won = true; }
    
    if (p.role === 'Pottan' && !p.eliminated && winningTeam === 'kallans') {
      pts = 2;
    }

    pointAssignments.push({ id: p.id, points: pts, wrongfulDeath, result: won ? 'win' : 'loss', caughtKallans: 0 });
  });

  // Calculate caughtKallan bonuses
  assignedRoles.forEach(p => {
    if (p.eliminated && p.role === 'Kallan' && p.caughtBy) {
      const hero = pointAssignments.find(pa => pa.id === p.caughtBy);
      if (hero) hero.caughtKallans += 1;
    }
  });

  return pointAssignments;
};
